public class actividad13{
    public static void main(String[]args){
        int i;
        int j;
        
        System.out.println("Tabla de Multiplcar del 1 al 10");
        System.out.println("======================================");
        for (i=1;i<=10;i++){
            for(j=1;j<=10;j++){
             System.out.println(i+" x "+j+" = "+(i*j));
            }
            System.out.println("=======================================");
        }
    }
}
